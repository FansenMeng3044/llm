"""Evaluate the current saved RL model against a reference baseline on each of
the five task-location distributions, and record the per-distribution gap.

The gap (RL vs baseline, paired on identical instances) is the signal the
adaptive loop feeds to DeepSeek to decide new training proportions: distributions
where the model is relatively weak get up-weighted.

Reference baseline is pluggable:
  * greedy  (default) -- the in-repo, train-free nearest-task heuristic. Always runnable.
  * ctasd            -- reads CTAS-D solution YAMLs (results.yaml) produced by the
                        external MILP solver; only usable with --testset-root that
                        already contains those solutions (see notes at bottom).

Instances come either freshly generated by seed (default) or loaded from a
pre-pickled per-distribution test-set folder (required for ctasd).

Usage:
    python eval_distributions.py                          # RL vs greedy, 30 fresh instances/dist
    python eval_distributions.py --instances 50 --rl-samples 10
    python eval_distributions.py --baseline ctasd --testset-root RALTestSet_5dist
Output: eval_gap.json (next to the model checkpoint).
"""
import os
import io
import json
import copy
import time
import glob
import pickle
import argparse
import contextlib

import numpy as np
import torch

from attention import AttentionNet
from worker import Worker
from env.task_env import TaskEnv
from parameters import EnvParams, TrainParams, SaverParams

LOCATION_DISTS = ['uniform', 'gmm', 'ring', 'bipolar', 'edges']
METRIC_KEYS = ['success_rate', 'makespan', 'waiting_time', 'travel_dist', 'efficiency']


def build_env(dist, seed, scale):
    per, spe, tasks = scale
    return TaskEnv((per, per), (spe, spe), (tasks, tasks), EnvParams.TRAIT_DIM,
                   EnvParams.DECISION_DIM, seed=seed, location_dist=dist)


def metrics_from_env(env, finished_tasks):
    """Mirror the per-instance metric extraction used in baseline/Greedy.py."""
    success = float(np.sum(finished_tasks) / len(finished_tasks))
    if success < 1:  # infeasible/deadlock -> only the success rate is meaningful
        return {'success_rate': success, 'makespan': np.nan, 'waiting_time': np.nan,
                'travel_dist': np.nan, 'efficiency': np.nan}
    return {'success_rate': success,
            'makespan': float(env.current_time),
            'waiting_time': float(np.mean(env.get_matrix(env.agent_dic, 'sum_waiting_time'))),
            'travel_dist': float(np.sum(env.get_matrix(env.agent_dic, 'travel_dist'))),
            'efficiency': float(np.mean(env.get_efficiency()))}


def run_rl(worker, env, rl_samples):
    """Run the trained policy on env; return its perf metrics (best of rl_samples runs)."""
    best = None
    sample = rl_samples > 1
    for _ in range(max(1, rl_samples)):
        env.init_state()
        worker.env = env
        with contextlib.redirect_stdout(io.StringIO()):
            _, _, results = worker.run_episode(False, sample, False)
        m = {k: (results[k][0] if k in results else np.nan) for k in METRIC_KEYS}
        if best is None or (m['success_rate'] >= best['success_rate']
                            and not (np.isnan(m['makespan'])) and
                            (np.isnan(best['makespan']) or m['makespan'] <= best['makespan'])):
            best = m
    return best


def run_greedy(env):
    env.init_state()
    with contextlib.redirect_stdout(io.StringIO()):
        env.execute_greedy_action()
    _, finished = env.get_episode_reward(EnvParams.MAX_TIME)
    return metrics_from_env(env, finished)


def run_ctasd(env, instance_dir):
    """Replay a precomputed CTAS-D solution (results.yaml) for this instance, if present."""
    import yaml
    param_f = os.path.join(instance_dir, 'planner_param.yaml')
    result_f = os.path.join(instance_dir, 'results.yaml')
    if not (os.path.exists(param_f) and os.path.exists(result_f)):
        return None  # no precomputed CTAS-D solution for this instance
    param = yaml.safe_load(open(param_f))
    num_veh = param['vehNum'] if param.get('flagSolver') == 'TEAMPLANNER_DET' else param['vehNumPerType']
    data = yaml.safe_load(open(result_f))
    if 'vehicle' not in data:
        return {'success_rate': 0.0, 'makespan': np.nan, 'waiting_time': np.nan,
                'travel_dist': np.nan, 'efficiency': np.nan}
    nodes = {i: [] for i in range(len(num_veh))}
    for v in data['vehicle'].values():
        nodes[v['type'] - 1].append(v['node'])
    env.init_state()
    species_pool = {s: list(env.species_dict[s]) for s in nodes}
    for species, routes in nodes.items():
        for route in routes:
            if not species_pool[species]:
                continue
            env.pre_set_route(route[1:], species_pool[species].pop(0))
    env.force_wait = True
    with contextlib.redirect_stdout(io.StringIO()):
        env.execute_by_route()
    _, finished = env.get_episode_reward(EnvParams.MAX_TIME)
    return metrics_from_env(env, finished)


def aggregate(per_instance):
    """Mean over instances; makespan/waiting/etc. averaged only over solved instances."""
    arr = {k: np.array([m[k] for m in per_instance], dtype=float) for k in METRIC_KEYS}
    out = {'instances': len(per_instance),
           'success_rate': float(np.nanmean(arr['success_rate']))}
    for k in ['makespan', 'waiting_time', 'travel_dist', 'efficiency']:
        vals = arr[k][~np.isnan(arr[k])]
        out[k] = float(np.mean(vals)) if len(vals) else None
    return out


def load_model(model_path, weights_key, device):
    net = AttentionNet(TrainParams.AGENT_INPUT_DIM, TrainParams.TASK_INPUT_DIM, TrainParams.EMBEDDING_DIM).to(device)
    ckpt = torch.load(os.path.join(model_path, 'checkpoint.pth'), map_location='cpu')
    key = weights_key if weights_key in ckpt else ('model' if 'model' in ckpt else 'best_model')
    net.load_state_dict(ckpt[key])
    net.eval()
    return net, ckpt.get('episode', None), key


def evaluate(model_path, instances, scale, baseline, rl_samples, weights_key, testset_root, device):
    net, episode, used_key = load_model(model_path, weights_key, device)
    worker = Worker(0, net, net, 0, device)

    report = {'episode': episode, 'weights_key': used_key, 'baseline': baseline,
              'scale': {'per_species': scale[0], 'species': scale[1], 'tasks': scale[2]},
              'per_dist': {}}

    for dist in LOCATION_DISTS:
        files = None
        if testset_root:
            files = sorted(glob.glob(os.path.join(testset_root, dist, 'env_*.pkl')))
        rl_runs, base_runs = [], []
        n = len(files) if files else instances
        for i in range(n):
            if files:
                env = pickle.load(open(files[i], 'rb'))
                inst_dir = files[i].replace('.pkl', '')
            else:
                env = build_env(dist, seed=10_000 + i, scale=scale)
                inst_dir = None

            rl_runs.append(run_rl(worker, env, rl_samples))

            if baseline == 'greedy':
                base_runs.append(run_greedy(env))
            elif baseline == 'ctasd':
                res = run_ctasd(env, inst_dir) if inst_dir else None
                if res is None:
                    raise SystemExit(
                        f"baseline=ctasd needs precomputed results.yaml per instance under "
                        f"{testset_root}/{dist}/env_i/. None found. See notes in eval_distributions.py.")
                base_runs.append(res)

        rl_agg, base_agg = aggregate(rl_runs), aggregate(base_runs)
        gap = {}
        for k in ['makespan', 'waiting_time', 'travel_dist', 'efficiency']:
            if rl_agg[k] is not None and base_agg[k] is not None:
                gap[f'{k}_rl_minus_base'] = round(rl_agg[k] - base_agg[k], 4)
        gap['success_rate_rl_minus_base'] = round(rl_agg['success_rate'] - base_agg['success_rate'], 4)
        report['per_dist'][dist] = {'rl': _round(rl_agg), 'baseline': _round(base_agg), 'gap': gap}
        print(f"{dist:9s}  RL makespan={rl_agg['makespan']}  base={base_agg['makespan']}  "
              f"RL succ={rl_agg['success_rate']:.3f}  base succ={base_agg['success_rate']:.3f}")

    out_path = os.path.join(model_path, 'eval_gap.json')
    with open(out_path, 'w') as f:
        json.dump(report, f, indent=2)
    print('wrote', out_path)
    return report


def _round(d):
    return {k: (round(v, 4) if isinstance(v, float) else v) for k, v in d.items()}


def main():
    ap = argparse.ArgumentParser(description='Per-distribution RL-vs-baseline evaluation.')
    ap.add_argument('--model', default=SaverParams.MODEL_PATH)
    ap.add_argument('--instances', type=int, default=30, help='instances per distribution (generate mode)')
    ap.add_argument('--baseline', choices=['greedy', 'ctasd'], default='greedy')
    ap.add_argument('--rl-samples', type=int, default=1, help='RL rollouts per instance, keep best (1=greedy argmax)')
    ap.add_argument('--weights-key', default='best_model', choices=['best_model', 'model'])
    ap.add_argument('--testset-root', default=None, help='dir with <dist>/env_*.pkl (required for ctasd)')
    ap.add_argument('--tasks', type=int, default=EnvParams.TASKS_RANGE[1])
    ap.add_argument('--species', type=int, default=EnvParams.SPECIES_RANGE[1])
    ap.add_argument('--per-species', type=int, default=EnvParams.SPECIES_AGENTS_RANGE[0])
    ap.add_argument('--device', default='cpu')
    args = ap.parse_args()

    scale = (args.per_species, args.species, args.tasks)
    evaluate(args.model, args.instances, scale, args.baseline, args.rl_samples,
             args.weights_key, args.testset_root, torch.device(args.device))


if __name__ == '__main__':
    main()
